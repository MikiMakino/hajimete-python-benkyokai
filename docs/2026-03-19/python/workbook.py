# %%
# ==============================================================================
# Lesson 5  結合に使うキーを確認する
# ==============================================================================
#
# 2つの表を結合するには、共通の「キー列」が必要です。
# 今回のキーは「商品コード」です。
#
# Excel の VLOOKUP でいうと：
#   =VLOOKUP(商品コード, 商品マスタ, 列番号, FALSE)
#   の「商品コード」にあたる部分です。

# 受注データの商品コードを確認する
print("受注データの商品コード：")
print(受注["商品コード"].unique())


# %%
# 商品マスタの商品コードを確認する
print("商品マスタの商品コード：")
print(マスタ["商品コード"].unique())

# ポイント：unique() は重複なしの値一覧を返します。
#           両方を見比べて、一致しているコードが結合できます。
#           受注に「P003」というコードがあっても、マスタになければ
#           それは「結合できないデータ」になります。


# %%
# キー列のデータ型を確認する
print("受注データ  商品コードの型：", 受注["商品コード"].dtype)
print("商品マスタ  商品コードの型：", マスタ["商品コード"].dtype)

# ポイント：型が違うと結合できません。
#           両方 object（文字列）であれば OK です。
#           片方が int などの場合は Lesson 6 で整えます。


# %%
# ==============================================================================
# Lesson 6  必要なら型と表記を整える（前処理）
# ==============================================================================
#
# 実務では、このままでは結合できないことがよくあります。
# よくある原因：
#   - 前後に空白が入っている（" A001" と "A001" は別物扱い）
#   - 型が違う（文字列 vs 数値）
#
# 今回のサンプルはそのまま結合できますが、
# 実務に備えて、整える処理を体験しておきましょう。

# 文字列型に揃えて、前後の空白を取る
受注["商品コード"] = 受注["商品コード"].astype(str).str.strip()
マスタ["商品コード"] = マスタ["商品コード"].astype(str).str.strip()

print("整形後 受注データ  商品コードの型：", 受注["商品コード"].dtype)
print("整形後 商品マスタ  商品コードの型：", マスタ["商品コード"].dtype)

# ポイント：
#   astype(str)  → 文字列型に変換
#   .str.strip() → 前後の空白を取り除く
#   この2つをセットで使うのが実務の定番です。


# %%
# 整形後に改めて確認する
print(受注["商品コード"].unique())
print(マスタ["商品コード"].unique())

# ポイント：整えたあとは必ず再確認しましょう。
#           「直したつもりでも直っていない」ことがあります。


# %%
# ==============================================================================
# Lesson 7  2つの表を結合する（まず成功させる）
# ==============================================================================
#
# merge() で2つの DataFrame を結合します。
# これは Excel の VLOOKUP に相当する処理です。
#
# 書き方：
#   pd.merge(左の表, 右の表, on="キー列名", how="left")
#
# how="left" とは？
#   左の表（受注データ）を基準にして、
#   右の表（商品マスタ）から一致する行を結合します。
#   Excel の VLOOKUP と同じ考え方です。
#   キーが一致しない行は NaN（空欄）になります。

結合済み = pd.merge(受注, マスタ, on="商品コード", how="left")

print("結合しました")
print("結合後の行数・列数：", 結合済み.shape)


# %%
# ==============================================================================
# Lesson 8  結合結果を確認する
# ==============================================================================
#
# 結合したら、必ず確認しましょう。

