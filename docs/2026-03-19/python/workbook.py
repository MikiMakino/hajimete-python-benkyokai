# %%
# 欠損（NaN）がある行を確認する
# NaN = 結合できなかった行（マスタに商品コードがなかった行）

print("欠損のある行数（列ごと）：")
print(結合済み.isna().sum())

# ポイント：isna().sum() で列ごとの欠損件数がわかります。
#           商品名や単価に欠損があれば「マスタ未登録データあり」です。


# %%
# ==============================================================================
# Lesson 9  必要な列だけ取り出す
# ==============================================================================
#
# 結合後は列が増えて見づらくなります。
# 必要な列だけ選んで、整理した表を作りましょう。
#
# 取り出す列：注文日、注文番号、商品コード、商品名、単価、数量、カテゴリ

必要列 = ["注文日", "注文番号", "商品コード", "商品名", "単価", "数量", "カテゴリ"]
整理済み = 結合済み[必要列]

整理済み.head(10)

# ポイント：列名のリストを [ ] で渡すと、その列だけの表が作れます。
#           「全部持つのではなく、必要なものだけ残す」が実務の基本です。


# %%
# ==============================================================================
# Lesson 10  新しい列を追加する
# ==============================================================================
#
# 数量 × 単価 で「金額」列を追加します。
# Excel でいうと、計算列を追加するイメージです。

整理済み = 整理済み.copy()  # 元のデータに影響が出ないようコピーする
整理済み["金額"] = 整理済み["数量"] * 整理済み["単価"]

整理済み.head(10)

# ポイント：列どうしの計算は * / + - がそのまま使えます。
#           計算結果を新しい列名で入れるだけです。
#           NaN のある行は金額も NaN になります（計算できないため）。


# %%
# 金額の合計を確認する
print("金額の合計：", 整理済み["金額"].sum())

# ポイント：sum() で合計を計算できます。
#           NaN の行は自動的に除外されます。


# %%
# ==============================================================================
# Lesson 11  結合できなかったデータを確認する
# ==============================================================================
#
# 結合できた件数だけでなく、「結合できなかったもの」も確認するのが大切です。
# 実務では、マスタ未登録データの発見やトラブル防止につながります。

# 商品名が NaN の行 = マスタに商品コードが存在しなかった行
未登録 = 整理済み[整理済み["商品名"].isna()]

print("マスタ未登録のデータ：")
print(未登録)


# %%
